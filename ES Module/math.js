
// For Addition of 2 numbers
const add = (a,b) => {
    return a+b;
};

// For Subtraction of 2 numbers
const subtract = (a,b) => {
    return a-b;
};

// For Multiplication of 2 numbers
const multi = (a,b) => {
    return a*b;
};

// For Division of 2 numbers
const division = (a,b) => {
    return a/b;
};

// Value of PI
const PI = 3.14;



// Export using ES Module in NodeJs..
export{add,subtract,multi,division,PI}  //Short Hand
